     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
      $request->validate([
          'name' => 'required',
          'duration' => 'required|numeric'
      ]);

        $slider_time = SliderTime::find($id);
        $slider_time->name = $request->name;
        $slider_time->duration = $request->duration;
        $slider_time->save();

        return redirect()->route('slider-time-maintains.index')->with('message', 'Slider Time updated successfully.');
    }

